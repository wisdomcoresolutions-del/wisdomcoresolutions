    end if;
    
    if not exists (select 1 from pg_policies where tablename = 'leads' and policyname = 'Allow authenticated users to update leads') then
        create policy "Allow authenticated users to update leads"
        on public.leads for update
        to authenticated
        using (true);
    end if;
    
    if not exists (select 1 from pg_policies where tablename = 'leads' and policyname = 'Allow authenticated users to select leads') then
        create policy "Allow authenticated users to select leads"
        on public.leads for select
        to authenticated
        using (true);
